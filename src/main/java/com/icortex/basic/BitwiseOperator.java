package com.icortex.basic;

public class BitwiseOperator {
    public static void main(String[] args) {
        int neg = -1;
        int pos = 1;
        System.out.println("Positive :"+Integer.toBinaryString(pos));
        System.out.println("Positive :"+Integer.toBinaryString(neg));

        int long_neg = -1;
        int long_pos = 1;
        System.out.println("Positive :"+Integer.toBinaryString(long_pos));
        System.out.println("Positive :"+Integer.toBinaryString(long_neg));
    }
}
